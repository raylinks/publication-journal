    <h4 class="text-prima"><strong>Manuscript Format</strong></h4>
    <p></p>
    All portions of the manuscript must be typed double-spaced and all pages numbered starting from the title page. Times new Romans (12 points) should be used throughout the manuscript. The Title should be a brief phrase describing the contents of the paper. 
    Manuscripts are to be arranged as follows:
