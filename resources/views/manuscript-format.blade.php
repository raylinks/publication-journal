            Tables are to be typed double-spaced throughout, including headings and footnotes. 
            Each table should be on a separate page, numbered consecutively in Roman numerals and supplied with a heading and a legend. 
            Tables should be self-explanatory without reference to the text. Tables should be prepared in Microsoft Word.
